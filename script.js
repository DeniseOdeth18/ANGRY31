//Ejemplos de los diferentes tipos de numeros en JavaScript

//Cadena
var string = "Esta es una cadena";
console.log(string);

//Numero
var num = 100;
console.log(num);

//Booleano
var bool = true;
console.log(bool);

//Indefinido
var object;
console.log(object);

//Reasignando el mismo objeto indefinido a nulo

//Nulo
object = null;
console.log(object);

//Ejemplos de matrices

//Una matriz que contiene el mismo tipo de datos
var arr1 = [1,2,3,4,5];
console.log(arr1);

var arr2 = ["nombre",12,true];
console.log(arr2);

//Una matriz que almacena una lista de matrices
var arr3 = [[1,2], [2,3], [3,4]];
console.log(arr3);

//Acceder al primer elemento de la matriz
console.log(arr3[0]);

//Acceder al segundo elemento de la matriz
console.log(arr3[0][1]);

arr3.push("mi nombre");
console.log(arr3);

arr3.pop();
console.log(arr3);























